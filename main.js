$( "#o1" ).on('mouseenter', function() {
  $('#root').css({"background": 'url(/images/2.jpg', "background-size": "cover", 'background-repeat': 'no-repeat', 'transition': 'all 0.25s ease', });
  $('#img').attr("src", "/images/Perkins.png");
}).on('mouseleave', function() {
  $('#root').css("background", 'rgb(29, 29, 29)', 'slow').dequeue();
  $('#img').attr("src", "/images/3.svg");
});


